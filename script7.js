// booleans & comparison operator

// booleans gives value
// true and false

let num1 = 7;
let num2 = 8;

console.log(num1>num2);
console.log(num1<num2);

// == vs ===
// == only check value not check datatype
let num3 = 7;
let num4 = "7";

console.log(num3 == num4);


// ===
// === check value and also datatype
let num5 = 7;
let num6 = "7"; //string datatype

console.log(num5 === num6);

// != vs !==
console.log(num3 != num4);
console.log(num3 !== num4);


