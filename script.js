// console is used to print something on console
console.log("hello world");

// introduce to variable
// variables can store some information
// we can use that information later
// we can change that information later

// declare a  variable

var firstName = "Aditi";
console.log(firstName);

// change value
firstName = "dipika";
console.log(firstName);

// rules for naming variables

// you cannot start with number
// example:-
// 1value(invalid)
// value1 (valid)

var value1 = 2;
console.log(value1);

value1 = 3;
console.log(value1);

// you can use underscore _ or dollar symbol
// first_name(valid)
// firstname (invalid)

// first$name (valid)
// $firstname (valid)

// you cannot use spaces

var first_name = "ritika"; //snake case
console.log(first_name);

var firstName = "yash"; //camel case
console.log(firstName);

// first name (invalid)

// convention
// start with small letter and use camelCase
