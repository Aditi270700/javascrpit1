// shift,unshift
// unshift is to add element from the start
let fruit = ['apple','mango','banana'];
console.log(fruit);
fruit.unshift('orange','guava');
console.log(fruit);


// shift
// shift is used to remove elements from the first

let fruits = ['apple','mango','banana'];
console.log(fruits);
fruits.shift();
console.log(fruits);

let fru = ['apple','mango','banana'];
console.log(fru);
let newFruit=fru.shift()
console.log("the remove fruit name is",newFruit);